/*Sayıyı 1 artırmak için "++" kullanılır "++" sola yazarsan değeri artırıp yazar. Sağa yazarsan yazdırıp değerini artırır.*/

var sayi = 10;

console.log(++sayi);


/*2 değerle işlem yaparken böyle yapılabilir yazdırıldığında iki sayıyı çarparak yazdırır.*/
